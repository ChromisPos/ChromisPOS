            createResourceString("uk/chromis/pos/templates/Role.Administrator.xml", "UPDATE ROLES SET PERMISSIONS = ? WHERE ID = '0'", "UPDATE ROLES SET RIGHTSLEVEL = 9 WHERE ID = '0'");
